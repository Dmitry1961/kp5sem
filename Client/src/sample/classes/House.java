package sample.classes;

public class House {
    public String idHouse;
    public String country;
    public String type;
    public String countRoom;
    public String festive;
    public String priceDay;

   public House (){};

    public String getIdHouse() {
        return idHouse;
    }

    public void setIdHouse(String idHouse) {
        this.idHouse = idHouse;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getCountRoom() {
        return countRoom;
    }

    public void setCountRoom(String countRoom) {
        this.countRoom = countRoom;
    }

    public String getFestive() {
        return festive;
    }

    public void setFestive(String festive) {
        this.festive = festive;
    }

    public String getPriceDay() {
        return priceDay;
    }

    public void setPriceDay(String priceDay) {
        this.priceDay = priceDay;
    }
}
